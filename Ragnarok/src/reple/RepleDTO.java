package reple;

public class RepleDTO {
	private int bbsID;
	private String userID;
	private String repleDATE;
	private String repleContent;
	private int repleAvailable;
	
	public int getBbsID() {
		return bbsID;
	}
	public void setBbsID(int bbsID) {
		this.bbsID = bbsID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getRepleDATE() {
		return repleDATE;
	}
	public void setRepleDATE(String repleDATE) {
		this.repleDATE = repleDATE;
	}
	public String getRepleContent() {
		return repleContent;
	}
	public void setRepleContent(String repleContent) {
		this.repleContent = repleContent;
	}
	public int getRepleAvailable() {
		return repleAvailable;
	}
	public void setRepleAvailable(int repleAvailable) {
		this.repleAvailable = repleAvailable;
	}
	
	
}
